import {
  BuyToken as BuyTokenEvent,
  Mint as MintEvent,
  SellToken as SellTokenEvent
} from "../generated/MockSwap/MockSwap"
import { BuyToken, Mint, SellToken } from "../generated/schema"

export function handleBuyToken(event: BuyTokenEvent): void {
  let entity = new BuyToken(
    event.transaction.hash.concatI32(event.logIndex.toI32())
  )
  entity.user = event.params.user
  entity.amount = event.params.amount

  entity.blockNumber = event.block.number
  entity.blockTimestamp = event.block.timestamp
  entity.transactionHash = event.transaction.hash

  entity.save()
}

